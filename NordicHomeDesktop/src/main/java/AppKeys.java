public class AppKeys {

    private String name, key, oldKey;
    private Integer index, boundNetKey;

    public AppKeys (String name, String key, String oldKey, Integer index, Integer boundNetKey) {
        this.name = name;
        this.key = key;
        this.oldKey = oldKey;
        this.index = index;
        this.boundNetKey = boundNetKey;
    }
}